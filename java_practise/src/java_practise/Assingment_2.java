package java_practise;

public class Assingment_2 {

	public static void main(String[] args) {
		char x='a';
		switch (x) {
		case 'a': 
			System.out.println("hi");
			break;
		case 'b':
			System.out.println("hello");
			break;
		case 'c':
			System.out.println("bye");
			break;
			default:
				System.out.println("invalid option");
		}

	}

}
